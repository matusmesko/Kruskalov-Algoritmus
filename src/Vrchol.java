public class Vrchol {

    private int komponent;
    private int cisloVrchola;

    public Vrchol(int komponent, int cisloVrchola) {
        this.komponent = komponent;
        this.cisloVrchola = cisloVrchola;
    }

    public int getKomponent() {
        return komponent;
    }

    public int getCisloVrchola() {
        return cisloVrchola;
    }

    public void setKomponent(int komponent) {
        this.komponent = komponent;
    }

    public void setCisloVrchola(int cisloVrchola) {
        this.cisloVrchola = cisloVrchola;
    }
}
